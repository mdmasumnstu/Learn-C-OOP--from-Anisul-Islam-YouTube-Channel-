#include<iostream>
using namespace std;
int main()
{
	//Integer type input
	int num;
	cout<<"Enter the number: "<<endl;
	cin>>num;
	cout<<"The number is: "<<num<<endl;
	
	//character type input
	char ch;
	cout<<"Enter the character: "<<endl;
	cin>>ch;
	cout<<"The character is: "<<ch<<endl;
	
	//String type input
	char a[20];
	cout<<"Enter the string: "<<endl;
	cin>>a;
	cout<<"The string is: "<<a<<endl;
	return 0;
}
